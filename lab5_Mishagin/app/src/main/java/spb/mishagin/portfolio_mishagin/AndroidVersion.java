package spb.mishagin.lab5_mishagin;

public class AndroidVersion {
    private final String name;
    private final int image;

    public AndroidVersion(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }

}


